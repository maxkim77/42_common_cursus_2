#include "../../inc/libft.h"

size_t	ft_strncpy(char *dst, const char *src, size_t dstsize)
{
	size_t		srcsize;
	size_t		i;

	srcsize = 0;
	while (src[srcsize])
		srcsize++;
	if (!dst || dstsize == 0)
		return (srcsize);
	i = 0;
	while (i < dstsize - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srcsize);
}
